cars = ["Ford", "Volvo", "BMW"]
for x in cars:  # Looping Array Elements
    print(x)

cars.append("Honda")   # Adding Array Elements
print(cars)

cars.pop(3)  # You can use the pop() method to remove an element from the array
print(cars)

cars.remove("Volvo")    # You can also use the remove() method to remove an element from the array.
print(cars)
